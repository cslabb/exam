DATA SEGMENT
    NUM DW 0005H
    FACT DW ?
    MSG DB 'FACTORIAL = $'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
    MOV AX, @DATA
    MOV DS, AX

    MOV AX, 0001H
    MOV CX, NUM

FACT_LOOP:
    MUL CX
    DEC CX
    JNZ FACT_LOOP

    MOV FACT, AX

    LEA DX, MSG
    MOV AH, 09H
    INT 21H

    MOV AX, FACT
    CALL DISP_NUM

    MOV AH, 4CH
    INT 21H

DISP_NUM PROC
    PUSH AX
    PUSH BX
    PUSH CX
    PUSH DX
    MOV CX, 0
    MOV BX, 10
CONVERT:
    XOR DX, DX
    DIV BX
    PUSH DX
    INC CX
    CMP AX, 0
    JNE CONVERT
PRINT_LOOP:
    POP DX
    ADD DL, 30H
    MOV AH, 02H
    INT 21H
    LOOP PRINT_LOOP
    POP DX
    POP CX
    POP BX
    POP AX
    RET
DISP_NUM ENDP

CODE ENDS
END START
