DATA SEGMENT
    NUM1 DW 0030H
    NUM2 DW 0024H
    GCD  DW ?
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, NUM1
    MOV BX, NUM2

COMPARE:
    CMP AX, BX
    JE EXIT
    
    JA GREATER
    XCHG AX, BX

GREATER:
    SUB AX, BX
    JMP COMPARE

EXIT:
    MOV GCD, AX
    
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START