DATA SEGMENT
    ARRAY   DW 0001H, 0002H, 0003H, 0004H, 0005H
    COUNT   DW 05H
    SUM     DW 0
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    LEA SI, ARRAY
    MOV CX, COUNT
    MOV AX, 0

LOOP1:
    ADD AX, [SI]
    ADD SI, 2
    DEC CX
    JNZ LOOP1

    MOV SUM, AX
    
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START