DATA SEGMENT
    ARRAY   DW 5555H, 1111H, 4444H, 2222H, 3333H
    COUNT   DW 05H
    SMALLEST DW ?
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    LEA SI, ARRAY
    MOV CX, COUNT
    MOV AX, [SI]
    
    DEC CX
    JZ EXIT
    
UP:
    ADD SI, 2
    CMP AX, [SI]
    JLE NO_CHANGE
    MOV AX, [SI]
    
NO_CHANGE:
    DEC CX
    JNZ UP
    
EXIT:
    MOV SMALLEST, AX
    
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START