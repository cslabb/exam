DATA SEGMENT
    ARRAY      DW 0001H, 0002H, 0003H, 0004H, 0005H
    COUNT      DW 05H
    EVEN_COUNT DW 0
    ODD_COUNT  DW 0
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    LEA SI, ARRAY
    MOV CX, COUNT
    MOV BX, 0
    MOV DX, 0

LOOP_START:
    MOV AX, [SI]
    TEST AX, 0001H
    JZ EVEN_NUM
    
    INC DX
    JMP NEXT_NUM

EVEN_NUM:
    INC BX
    
NEXT_NUM:
    ADD SI, 2
    DEC CX
    JNZ LOOP_START

    MOV EVEN_COUNT, BX
    MOV ODD_COUNT, DX
    
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START