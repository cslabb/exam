DATA SEGMENT
    NUM1 DW 0005H
    NUM2 DW 0010H
    SUM  DW 0
    CARRY DW 0
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    MOV AX, NUM1
    MOV BX, NUM2
    ADD AX, BX
    
    JNC NO_CARRY
    MOV CARRY, 0001H

NO_CARRY:
    MOV SUM, AX
    
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START