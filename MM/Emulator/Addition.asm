DATA SEGMENT
    NUM1 DW 0005H
    NUM2 DW 0010H
    SUM  DW ?
    CARRY DW ?
    MSG1 DB 'SUM = $'
    MSG2 DB 0AH,0DH,'CARRY = $'
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS:DATA
START:
    MOV AX, @DATA
    MOV DS, AX

    MOV AX, NUM1
    ADD AX, NUM2
    JNC NO_CARRY
    MOV CARRY, 1
    JMP STORE
NO_CARRY:
    MOV CARRY, 0
STORE:
    MOV SUM, AX

    LEA DX, MSG1
    MOV AH, 09H
    INT 21H

    MOV AX, SUM
    CALL DISP_NUM

    LEA DX, MSG2
    MOV AH, 09H
    INT 21H

    MOV AX, CARRY
    CALL DISP_NUM

    MOV AH, 4CH
    INT 21H

DISP_NUM PROC
    PUSH AX
    PUSH BX
    PUSH CX
    PUSH DX
    MOV CX, 0
    MOV BX, 10
CONVERT:
    XOR DX, DX
    DIV BX
    PUSH DX
    INC CX
    CMP AX, 0
    JNE CONVERT
PRINT_LOOP:
    POP DX
    ADD DL, 30H
    MOV AH, 02H
    INT 21H
    LOOP PRINT_LOOP
    POP DX
    POP CX
    POP BX
    POP AX
    RET
DISP_NUM ENDP

CODE ENDS
END START
