DATA SEGMENT
    NUM      DB 10110010B
    ONE_COUNT DB 0
DATA ENDS

CODE SEGMENT
    ASSUME CS:CODE, DS:DATA
START:
    MOV AX, DATA
    MOV DS, AX

    MOV AL, NUM
    MOV CX, 8
    MOV BL, 0

LOOP1:
    SHL AL, 1
    JNC NEXT_BIT
    INC BL

NEXT_BIT:
    DEC CX
    JNZ LOOP1

    MOV ONE_COUNT, BL
    
    MOV AH, 4CH
    INT 21H
CODE ENDS
END START